// ===== Compile helpers (ported from train_large.m lines 59-100) =====
static bool compile_layer_kernels(LayerKernels *lk, LayerWeights *w, int *compile_count) {
    lk->fwdAttn = compile_kern(gen_sdpa_fwd_taps(), (@{
        @"@model_path/weights/rms1.bin": @{@"offset":@0, @"data":build_blob(w->rms_att, 1, DIM)},
        @"@model_path/weights/wq.bin":   @{@"offset":@0, @"data":build_blob(w->Wq, DIM, DIM)},
        @"@model_path/weights/wk.bin":   @{@"offset":@0, @"data":build_blob(w->Wk, DIM, DIM)},
        @"@model_path/weights/wv.bin":   @{@"offset":@0, @"data":build_blob(w->Wv, DIM, DIM)},
        @"@model_path/weights/wo.bin":   @{@"offset":@0, @"data":build_blob(w->Wo, DIM, DIM)},
        @"@model_path/weights/mask.bin": @{@"offset":@0, @"data":get_mask_blob()},
    }), DIM*SEQ*2, 6*DIM*SEQ*2);

    lk->fwdFFN = compile_kern(gen_ffn_fwd_taps(), (@{
        @"@model_path/weights/rms2.bin": @{@"offset":@0, @"data":build_blob(w->rms_ffn, 1, DIM)},
        @"@model_path/weights/w1.bin":   @{@"offset":@0, @"data":build_blob(w->W1, HIDDEN, DIM)},
        @"@model_path/weights/w3.bin":   @{@"offset":@0, @"data":build_blob(w->W3, HIDDEN, DIM)},
        @"@model_path/weights/w2.bin":   @{@"offset":@0, @"data":build_blob(w->W2, DIM, HIDDEN)},
    }), DIM*SEQ*2, (2*DIM + 3*HIDDEN)*SEQ*2);

    lk->ffnBwd = compile_kern(gen_ffn_bwd(), (@{
        @"@model_path/weights/w2t.bin": @{@"offset":@0, @"data":build_blob_t(w->W2, DIM, HIDDEN)},
        @"@model_path/weights/w1t.bin": @{@"offset":@0, @"data":build_blob_t(w->W1, HIDDEN, DIM)},
        @"@model_path/weights/w3t.bin": @{@"offset":@0, @"data":build_blob_t(w->W3, HIDDEN, DIM)},
    }), (DIM + 2*HIDDEN)*SEQ*2, (DIM + 2*HIDDEN)*SEQ*2);

    lk->sdpaBwd1 = compile_kern(gen_sdpa_bwd1(), (@{
        @"@model_path/weights/mask.bin": @{@"offset":@0, @"data":get_mask_blob()},
        @"@model_path/weights/wot.bin":  @{@"offset":@0, @"data":build_blob_t(w->Wo, DIM, DIM)},
    }), 4*DIM*SEQ*2, (DIM + 2*SCORE_CH)*SEQ*2);

    lk->qkvBwd = compile_kern(gen_qkvb(), (@{
        @"@model_path/weights/wqt.bin": @{@"offset":@0, @"data":build_blob_t(w->Wq, DIM, DIM)},
        @"@model_path/weights/wkt.bin": @{@"offset":@0, @"data":build_blob_t(w->Wk, DIM, DIM)},
        @"@model_path/weights/wvt.bin": @{@"offset":@0, @"data":build_blob_t(w->Wv, DIM, DIM)},
    }), 3*DIM*SEQ*2, DIM*SEQ*2);

    *compile_count += KERNELS_PER_LAYER;
    return lk->fwdAttn && lk->fwdFFN && lk->ffnBwd && lk->sdpaBwd1 && lk->qkvBwd;
}

static Kern *compile_sdpa_bwd2_kern(int *compile_count) {
    Kern *k = compile_kern(gen_sdpa_bwd2(), @{},
        (2*SCORE_CH + 2*DIM)*SEQ*2, 2*DIM*SEQ*2);
    if (k) (*compile_count)++;
    return k;
}

static void free_layer_kernels(LayerKernels *lk) {
    free_kern(lk->fwdAttn); free_kern(lk->fwdFFN); free_kern(lk->ffnBwd);
    free_kern(lk->sdpaBwd1); free_kern(lk->qkvBwd);
    lk->fwdAttn = lk->fwdFFN = lk->ffnBwd = lk->sdpaBwd1 = lk->qkvBwd = NULL;
}

// ===== Weight loading from llama2.c format =====
static bool load_pretrained(LayerWeights *lw, float *rms_final, float *embed, const char *path) {
    FILE *f = fopen(path, "rb");
    if (!f) return false;
    Llama2Config cfg;
    fread(&cfg, sizeof(cfg), 1, f);
    if (cfg.dim != DIM || cfg.hidden_dim != HIDDEN || cfg.n_layers != NLAYERS) {
        fclose(f); return false;
    }
    int V = abs(cfg.vocab_size);
    fread(embed, 4, V * DIM, f);
    for (int L = 0; L < NLAYERS; L++) fread(lw[L].rms_att, 4, DIM, f);
    for (int L = 0; L < NLAYERS; L++) fread(lw[L].Wq, 4, WQ_SZ, f);
    for (int L = 0; L < NLAYERS; L++) fread(lw[L].Wk, 4, WQ_SZ, f);
    for (int L = 0; L < NLAYERS; L++) fread(lw[L].Wv, 4, WQ_SZ, f);
    for (int L = 0; L < NLAYERS; L++) fread(lw[L].Wo, 4, WO_SZ, f);
    for (int L = 0; L < NLAYERS; L++) fread(lw[L].rms_ffn, 4, DIM, f);
    for (int L = 0; L < NLAYERS; L++) fread(lw[L].W1, 4, W1_SZ, f);
    for (int L = 0; L < NLAYERS; L++) fread(lw[L].W2, 4, W2_SZ, f);
    for (int L = 0; L < NLAYERS; L++) fread(lw[L].W3, 4, W3_SZ, f);
    fread(rms_final, 4, DIM, f);
    fclose(f);
    return true;
}

// ===== Random init (same as macOS lines 253-269) =====
static void random_init_weights(LayerWeights *lw, float *rms_final, float *embed) {
    srand48(42);
    float scale_d = 1.0f / sqrtf(DIM), scale_h = 1.0f / sqrtf(HIDDEN);
    float out_scale = 1.0f / sqrtf((float)NLAYERS);
    for (int L = 0; L < NLAYERS; L++) {
        for (size_t i = 0; i < WQ_SZ; i++) {
            lw[L].Wq[i] = scale_d * (2*drand48()-1);
            lw[L].Wk[i] = scale_d * (2*drand48()-1);
        }
        for (size_t i = 0; i < WQ_SZ; i++) {
            lw[L].Wv[i] = scale_d * (2*drand48()-1);
            lw[L].Wo[i] = scale_d * out_scale * (2*drand48()-1);
        }
        for (size_t i = 0; i < W1_SZ; i++) lw[L].W1[i] = scale_h * (2*drand48()-1);
        for (size_t i = 0; i < W2_SZ; i++) lw[L].W2[i] = scale_d * out_scale * (2*drand48()-1);
        for (size_t i = 0; i < W3_SZ; i++) lw[L].W3[i] = scale_h * (2*drand48()-1);
        for (int i = 0; i < DIM; i++) { lw[L].rms_att[i] = 1.0f; lw[L].rms_ffn[i] = 1.0f; }
    }
    for (int i = 0; i < DIM; i++) rms_final[i] = 1.0f;
    float escale = 0.02f;
    for (size_t i = 0; i < (size_t)VOCAB * DIM; i++) embed[i] = escale * (2*drand48()-1);
}

// ===== Compile all kernels =====
static bool compile_all_kernels(ANETrainState *s) {
    s->compiling = true;

    // Free existing weight-bearing kernels
    for (int L = 0; L < NLAYERS; L++) free_layer_kernels(&s->kern[L]);

    // Compile weight-bearing kernels for all layers
    for (int L = 0; L < NLAYERS; L++) {
        if (!compile_layer_kernels(&s->kern[L], &s->lw[L], &s->compile_count)) {
            fprintf(stderr, "[engine] Compile failed at layer %d\n", L);
            s->compiling = false;
            return false;
        }
    }

    // Re-compile sdpaBwd2 if needed (weight-free, one per layer)
    for (int L = 0; L < NLAYERS; L++) {
        if (!s->sdpaBwd2[L]) {
            s->sdpaBwd2[L] = compile_sdpa_bwd2_kern(&s->compile_count);
            if (!s->sdpaBwd2[L]) {
                fprintf(stderr, "[engine] sdpaBwd2 compile failed at layer %d\n", L);
                s->compiling = false;
                return false;
            }
        }
    }

    s->kernels_compiled = true;
    s->compiling = false;
    return true;
}

// ===== iOS compile budget reset (no exec()) =====
static bool check_and_reset_compile_budget(ANETrainState *s) {
    if (s->compile_count + TOTAL_WEIGHT_KERNELS + NLAYERS <= IOS_MAX_COMPILES) return true;

    // Budget exhausted: free all kernels, reset counter, recompile
    fprintf(stderr, "[engine] Compile budget reached (%d), resetting...\n", s->compile_count);
    for (int L = 0; L < NLAYERS; L++) {
        free_layer_kernels(&s->kern[L]);
        free_kern(s->sdpaBwd2[L]);
        s->sdpaBwd2[L] = NULL;
    }
    s->compile_count = 0;
    s->kernels_compiled = false;
    return compile_all_kernels(s);
}

// ===== Public API: ane_train_init =====
ANETrainState *ane_train_init(const char *model_path, const char *data_path) {
    @autoreleasepool {
    ane_init();

    ANETrainState *s = (ANETrainState *)calloc(1, sizeof(ANETrainState));
    s->lr = 3e-4f;
    s->total_steps = 10000;
    s->last_loss = 999.0f;

    // Allocate per-layer state
    for (int L = 0; L < NLAYERS; L++) {
        s->lw[L] = layer_weights_alloc();
        s->la[L] = layer_adam_alloc();
        s->acts[L] = layer_acts_alloc();
        s->grads[L] = layer_grads_alloc();
        memset(&s->kern[L], 0, sizeof(LayerKernels));
        s->sdpaBwd2[L] = NULL;
    }

    // Global weights + optimizer
    s->rms_final = (float *)malloc(DIM * 4);
    s->embed = (float *)malloc(VOCAB * DIM * 4);
    s->grms_final = (float *)calloc(DIM, 4);
    s->gembed = (float *)calloc(VOCAB * DIM, 4);
    s->arms_final = adam_alloc(DIM);
    s->aembed = adam_alloc((size_t)VOCAB * DIM);

    // Scratch buffers
    s->x_cur     = (float *)malloc(SEQ * DIM * 4);
    s->x_final   = (float *)malloc(SEQ * DIM * 4);
    s->logits    = (float *)malloc(SEQ * VOCAB * 4);
    s->dlogits   = (float *)malloc(SEQ * VOCAB * 4);
    s->dy        = (float *)malloc(SEQ * DIM * 4);
    s->dffn      = (float *)malloc(SEQ * DIM * 4);
    s->dh1       = (float *)malloc(SEQ * HIDDEN * 4);
    s->dh3       = (float *)malloc(SEQ * HIDDEN * 4);
    s->dx_ffn    = (float *)malloc(SEQ * DIM * 4);
    s->dx2       = (float *)malloc(SEQ * DIM * 4);
    s->do_out_buf= (float *)malloc(SEQ * DIM * 4);
    s->dq        = (float *)malloc(SEQ * DIM * 4);
    s->dk        = (float *)malloc(SEQ * DIM * 4);
    s->dv        = (float *)malloc(SEQ * DIM * 4);
    s->dx_attn   = (float *)malloc(SEQ * DIM * 4);

    // Load weights: pretrained or random
    bool loaded = false;
    if (model_path) {
        loaded = load_pretrained(s->lw, s->rms_final, s->embed, model_path);
    }
    if (!loaded) {
        random_init_weights(s->lw, s->rms_final, s->embed);
    }

    // mmap token data
    s->data_fd = -1;
    s->token_data = NULL;
    s->n_tokens = 0;
    if (data_path) {
        s->data_fd = open(data_path, O_RDONLY);
        if (s->data_fd >= 0) {
            struct stat st;
            fstat(s->data_fd, &st);
            s->data_len = st.st_size;
            s->token_data = (uint16_t *)mmap(NULL, s->data_len, PROT_READ, MAP_PRIVATE, s->data_fd, 0);
            if (s->token_data == MAP_FAILED) {
                s->token_data = NULL;
                close(s->data_fd);
                s->data_fd = -1;
            } else {
                s->n_tokens = s->data_len / 2;
            }
        }
    }

    // Concurrency
    s->dw_q = dispatch_queue_create("ane.train.dw_cblas", DISPATCH_QUEUE_SERIAL);
    s->dw_grp = dispatch_group_create();

    // Compile initial kernels
    // sdpaBwd2 first (weight-free, one per layer)
    for (int L = 0; L < NLAYERS; L++) {
        s->sdpaBwd2[L] = compile_sdpa_bwd2_kern(&s->compile_count);
    }
    compile_all_kernels(s);

    // Zero gradient accumulators for first batch
    for (int L = 0; L < NLAYERS; L++) layer_grads_zero(&s->grads[L]);
    memset(s->grms_final, 0, DIM * 4);
    memset(s->gembed, 0, (size_t)VOCAB * DIM * 4);

    return s;
    }
}

